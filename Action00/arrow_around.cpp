//===========================================================================================
// 描画処理
//===========================================================================================
void CArrowAround::Draw(void)
{
	//デバイスの取得
	LPDIRECT3DDEVICE9 pDevice = CManager::GetRenderer()->GetDevice();

	//ライティングの有効/無効の設定
	pDevice->SetRenderState(D3DRS_LIGHTING, FALSE);

	CBillboard::Draw();

	//ライティングの有効/無効の設定
	pDevice->SetRenderState(D3DRS_LIGHTING, TRUE);
}