//===========================================================================================
// 生成
//===========================================================================================
CArrowAround *CArrowAround::Create()
{
	CArrowAround *m_arrowAround = nullptr;

	if (m_arrowAround == nullptr)
	{
		m_arrowAround = new CArrowAround;

		if (m_arrowAround != nullptr)
		{
			m_arrowAround->Init();

			return m_arrowAround;
		}

		return nullptr;
	}

	return m_arrowAround;
}

//===========================================================================================
// 初期化処理
//===========================================================================================
HRESULT CArrowAround::Init(void)
{
	CBillboard::Init();
	SetSize(D3DXVECTOR2(50.0f, 50.0f));

	return S_OK;
}

